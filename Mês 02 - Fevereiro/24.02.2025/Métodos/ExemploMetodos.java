/**
 * Exemplo de métodos em Java
 */
public class ExemploMetodos {

	/**
	 * Método para imprimir um texto na tela
	 */
	public static void imprimirTexto() {
		System.out.println("Hello, world!");
		System.out.println("Eu estou usando métodos pela primeira vez");
	}

	public static void main(String[] args) {
		// Chamando o método para imprimir o texto
		imprimirTexto();
	}

}
